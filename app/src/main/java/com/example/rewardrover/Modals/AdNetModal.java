package com.example.rewardrover.Modals;

public class AdNetModal {
    int adNetImg;
    String adNetName;

    public AdNetModal(int adNetImg, String adNetName) {
        this.adNetImg = adNetImg;
        this.adNetName = adNetName;
    }

    public int getAdNetImg() {
        return adNetImg;
    }

    public String getAdNetName() {
        return adNetName;
    }
}
